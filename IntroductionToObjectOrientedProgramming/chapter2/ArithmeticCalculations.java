package IntroductionToObjectOrientedProgramming.chapter2;

public class ArithmeticCalculations {

    public static void main(String[] args) {
        int a = 5;
        int b = 12;

        System.out.println(a+" + " + b + " = " + (a+b) );
        System.out.println(a+" / " + b + " = " + (a/b) );
        System.out.println(a+" * " + b + " = " + (a*b) );
        System.out.println(a+" - " + b + " = " + (a-b) );
    }
}
